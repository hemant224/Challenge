	public static void createPetitions(String firstName, String lastName, String email) {
		System.setProperty("webdriver.gecko.driver", 
				System.getProperty("user.dir")+"/src/test/resources/drivers/geckodriver");
		
		WebDriver driver = new FirefoxDriver();
		driver.get("http://chng.it/ChFB4pVdHn");
		driver.findElement(By.id("firstName")).sendKeys(firstName+getSaltString());
		driver.findElement(By.id("lastName")).sendKeys(lastName+firstName+getSaltString());
		
		driver.findElement(By.id("email")).sendKeys(firstName+lastName+getSaltString()+"@"+email+".com");
		driver.findElement(By.xpath("//button/span")).click();
		try {
			Thread.sleep(4000);
		} catch (InterruptedException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		driver.quit();
	}
